private void bButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bButtonMouseClicked
    if ( pop != null && index > 0 ) {
        index--;
        this.image = pop[index].getImage();
        ((BaseDisplayer)bimage).image = this.image;
        size.setText("Chromosome " + (index + 1));
        this.repaint();
    }
}//GEN-LAST:event_bButtonMouseClicked

