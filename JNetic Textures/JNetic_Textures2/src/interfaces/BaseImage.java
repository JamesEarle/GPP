private void fButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fButtonMouseClicked

    if ( pop != null && index < pop.length - 1 ) {
        index++;
        this.image = pop[index].getImage();
        ((BaseDisplayer)bimage).image = this.image;
        size.setText("Chromosome " + (index + 1));
        this.repaint();
    }
}//GEN-LAST:event_fButtonMouseClicked

